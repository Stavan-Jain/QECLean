"""Support-generates-G degeneracy classifier for BB polynomials.

A polynomial `A ∈ F_2[G]` is **non-degenerate** here in the structural
sense used by Lin–Pryadko 2306.16400 §III.C (and many earlier 2BGA
references): the additive subgroup `⟨supp(A)⟩ ≤ G` generated by its
support equals `G`. Equivalently, the index `[G : ⟨supp(A)⟩] = 1`.

A BB code `BB(G, A, B)` is non-degenerate iff **both** `A` and `B` are
non-degenerate in that sense. When one of the supports generates a
proper subgroup `H < G`, the BB construction effectively lives on
`F_2[H]`-cosets and the code factors as a direct product of smaller
BB codes (or trivially repeats them). Bravyi–Cross et al. (2308.07915)
implicitly require non-degenerate `A, B` for their constructions — the
gross polynomials each cover the full `Z_12 × Z_6`.

The classifier is intentionally cheap: BFS the subgroup closure under
the abelian group operation, stop on the first iteration that doesn't
add anything new, compare cardinality to `|G|`. For small BB groups
(`|G| ≤ 144` in the corpus), this is microseconds.

This module is read-only on its inputs — it does not modify polynomial
or group state.
"""

from __future__ import annotations

from .group import AbelianGroup
from .poly import Poly


def _subgroup_closure(
    generators: frozenset[tuple[int, ...]], G: AbelianGroup
) -> set[tuple[int, ...]]:
    """BFS the additive subgroup `⟨generators⟩ ≤ G`.

    The closure is the smallest subset of G containing 0 and closed
    under addition with each generator. (Since `G` is finite abelian
    and additively-written, taking inverses is automatic: every element
    has finite additive order.)

    Algorithm: start from `{0}`; iteratively add `h + g` for every
    already-known `h` and every generator `g`. Stop when no new
    element is added. Total iterations bounded by `|⟨generators⟩|`.
    """
    closure: set[tuple[int, ...]] = {tuple(0 for _ in G.orders)}
    if not generators:
        return closure
    frontier: set[tuple[int, ...]] = set(closure)
    while frontier:
        new_frontier: set[tuple[int, ...]] = set()
        for h in frontier:
            for g in generators:
                gh = G.add(h, g)
                if gh not in closure:
                    closure.add(gh)
                    new_frontier.add(gh)
        frontier = new_frontier
    return closure


def supp_generates_G(poly: Poly, G: AbelianGroup | None = None) -> bool:
    """Return True iff `⟨supp(poly)⟩ = G` (as additive subgroups of G).

    The closure is computed over the actual support set, regardless of
    F_2 coefficient values (which are all 1 by Poly's representation).

    Notes
    -----
    * The zero polynomial (empty support) generates only `{0}`; it
      returns `False` for any nontrivial `G`.
    * The constant polynomial `1` (support `{0}`) also generates only
      `{0}` — returns `False` for `|G| > 1`.
    * For Bravyi's gross polynomials `A = x³+y+y²`, `B = y³+x+x²` on
      `G = Z_12 × Z_6`, both generate the full G (return `True`).
    """
    if G is None:
        G = poly.group
    elif G != poly.group:
        raise ValueError("poly.group != G")
    closure = _subgroup_closure(poly.support, G)
    return len(closure) == G.cardinality


def support_subgroup_index(poly: Poly, G: AbelianGroup | None = None) -> int:
    """Return `[G : ⟨supp(poly)⟩]`.

    Equals 1 iff `poly` is non-degenerate (i.e. `supp_generates_G` is
    True). Useful as a diagnostic: large index means the polynomial
    lives in a small coset of G and the corresponding BB-block is a
    direct sum of smaller copies.

    Lagrange's theorem on abelian groups guarantees this index always
    divides `|G|`.
    """
    if G is None:
        G = poly.group
    elif G != poly.group:
        raise ValueError("poly.group != G")
    closure_size = len(_subgroup_closure(poly.support, G))
    if G.cardinality % closure_size != 0:
        raise RuntimeError(
            f"support_subgroup_index: closure size {closure_size} does not "
            f"divide |G| = {G.cardinality}; this violates Lagrange and "
            "indicates a bug in _subgroup_closure or AbelianGroup.add."
        )
    return G.cardinality // closure_size


def is_non_degenerate(
    A: Poly, B: Poly, G: AbelianGroup | None = None
) -> bool:
    """Return True iff **both** `A` and `B` have support generating G.

    This is the BB-code-level non-degeneracy condition: `BB(G, A, B)`
    is non-degenerate iff `⟨supp(A)⟩ = G = ⟨supp(B)⟩`.

    If either polynomial is degenerate (its support generates a proper
    subgroup), the BB construction either factors through a smaller
    group or produces an effectively-smaller code. The corpus has been
    observed to split roughly 50/50 between non-degenerate and
    degenerate codes (see notes/T3R2.2_nondegenerate_eval.md).
    """
    if G is None:
        G = A.group
    if A.group != B.group:
        raise ValueError("A and B must live in the same group algebra")
    return supp_generates_G(A, G) and supp_generates_G(B, G)


# ===========================================================================
# C-v3: elementary-abelian G_odd classifier
# ===========================================================================
#
# `G_odd` is the maximal odd-order quotient of an abelian group G — equivalently,
# the product of axis-wise odd parts. The C-v3 narrowed conjecture (HANDOFF_C3 §2)
# gates on G_odd being "elementary abelian" in the LOOSE sense:
#
#     for each prime p | |G_odd|, the p-primary part of G_odd is ≅ (Z_p)^{k_p}
#
# Equivalently: each per-axis odd order is squarefree (no prime-square factor).
#
# This includes bb_90's G_odd = Z_3 × Z_3 × Z_5 (two primes but each part
# elementary abelian) and excludes bb_108's G_odd = Z_9 × Z_3 (a Z_9 factor
# breaks the squarefree-axis-order condition).
#
# A stricter notion — `G_odd = (Z_p)^k` for a single prime p — is also exposed
# via `g_odd_elementary_prime` for callers that need the single-prime form.


def _prime_factorization(n: int) -> list[tuple[int, int]]:
    """Return [(p, e), …] for n = ∏ p^e, with p ascending. n must be ≥ 1.

    For n = 1, returns [] (the empty factorization).
    """
    if n < 1:
        raise ValueError(f"_prime_factorization: n = {n} must be ≥ 1")
    out: list[tuple[int, int]] = []
    p = 2
    m = n
    while p * p <= m:
        if m % p == 0:
            e = 0
            while m % p == 0:
                m //= p
                e += 1
            out.append((p, e))
        p += 1
    if m > 1:
        out.append((m, 1))
    return out


def _odd_part(n: int) -> int:
    """Return the largest odd divisor of n. For n = 0, returns 0."""
    while n > 0 and n % 2 == 0:
        n //= 2
    return n


def g_odd_decomposition(G: AbelianGroup) -> tuple[int, ...]:
    """Return the **primary cyclic factor orders** of the max-odd quotient `G_odd`.

    For G = ∏ Z_{n_i}, G_odd = ∏ Z_{n_i_odd} where n_i_odd = largest odd
    divisor of n_i. Each Z_{n_i_odd} decomposes via CRT into its prime-
    power cyclic components: Z_{n_i_odd} ≅ ∏_p Z_{p^{e_{ip}}} where
    n_i_odd = ∏ p^{e_{ip}}. The full primary cyclic decomposition of
    G_odd is the concatenation of all such prime-power factors across
    axes. The returned tuple is sorted ascending for determinism.

    Examples
    --------
    >>> g_odd_decomposition(ZmZn(12, 6))     # G_odd = Z_3 × Z_3
    (3, 3)
    >>> g_odd_decomposition(ZmZn(9, 6))      # G_odd = Z_9 × Z_3
    (3, 9)
    >>> g_odd_decomposition(ZmZn(15, 3))     # G_odd = Z_3 × Z_3 × Z_5
    (3, 3, 5)
    >>> g_odd_decomposition(AbelianGroup((4,)))  # G_odd trivial
    ()
    """
    factors: list[int] = []
    for axis_order in G.orders:
        odd_part = _odd_part(axis_order)
        if odd_part == 1:
            continue
        for p, e in _prime_factorization(odd_part):
            factors.append(p ** e)
    return tuple(sorted(factors))


def is_g_odd_elementary_abelian(G: AbelianGroup) -> bool:
    """Return True iff `G_odd` is **elementary abelian on every prime part**.

    Definition (this module's convention, per HANDOFF_C3 §6): for every
    prime `p` dividing `|G_odd|`, the p-primary subgroup of `G_odd` is
    a direct sum of copies of `Z_p` (NO `Z_{p^2}` or higher). Multiple
    distinct primes are allowed.

    Equivalently: every primary cyclic factor returned by
    `g_odd_decomposition` is prime (not a prime power > p^1).

    Equivalently: each axis's max-odd order is squarefree.

    For a trivial G_odd (G a 2-group), this is vacuously True.

    Examples
    --------
    >>> is_g_odd_elementary_abelian(ZmZn(12, 6))  # G_odd = (Z_3)^2
    True
    >>> is_g_odd_elementary_abelian(ZmZn(9, 6))   # G_odd has Z_9 — squarefree-fail
    False
    >>> is_g_odd_elementary_abelian(ZmZn(15, 3))  # G_odd = Z_3 × Z_3 × Z_5
    True
    >>> is_g_odd_elementary_abelian(AbelianGroup((4,)))  # G_odd trivial
    True

    Note
    ----
    This is the **loose** definition: multiple primes are allowed as
    long as each prime's primary part is elementary abelian. For the
    stricter "single-prime" definition, use `g_odd_elementary_prime`.
    """
    for factor in g_odd_decomposition(G):
        # factor is a prime power p^e. Check e == 1.
        # We know factor ≥ 3 (odd and > 1), so the smallest prime factor
        # is at least 3.
        # Simplest check: factor must be prime.
        if not _is_prime(factor):
            return False
    return True


def _is_prime(n: int) -> bool:
    """Quick primality test for small n (up to ~10^6 cleanly). For BB-corpus
    inputs (axis odd parts ≤ |G| ≤ 288), trial division is fine."""
    if n < 2:
        return False
    if n < 4:
        return True
    if n % 2 == 0:
        return False
    p = 3
    while p * p <= n:
        if n % p == 0:
            return False
        p += 2
    return True


def g_odd_elementary_prime(G: AbelianGroup) -> int | None:
    """Return the unique prime `p` if `G_odd ≅ (Z_p)^k` for some k ≥ 0,
    else None.

    This is the STRICT elementary-abelian condition: G_odd is a single-prime
    elementary abelian group. `bb_90_8_10` has `G_odd = Z_3 × Z_3 × Z_5`
    (two primes), so it returns None even though
    `is_g_odd_elementary_abelian` returns True.

    For trivial G_odd (G a 2-group), returns None — there is no prime.

    Examples
    --------
    >>> g_odd_elementary_prime(ZmZn(12, 6))  # G_odd = (Z_3)^2
    3
    >>> g_odd_elementary_prime(ZmZn(15, 3))  # multi-prime
    >>> g_odd_elementary_prime(ZmZn(9, 6))   # not elementary abelian
    """
    factors = g_odd_decomposition(G)
    if not factors:
        return None
    primes = set(factors)
    if len(primes) != 1:
        return None
    (p,) = primes
    if not _is_prime(p):
        return None
    return p
