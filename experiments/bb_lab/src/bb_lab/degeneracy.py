"""Support-generates-G degeneracy classifier for BB polynomials.

A polynomial `A ∈ F_2[G]` is **non-degenerate** here in the structural
sense used by Lin–Pryadko 2306.16400 §III.C (and many earlier 2BGA
references): the additive subgroup `⟨supp(A)⟩ ≤ G` generated by its
support equals `G`. Equivalently, the index `[G : ⟨supp(A)⟩] = 1`.

A BB code `BB(G, A, B)` is non-degenerate iff **both** `A` and `B` are
non-degenerate in that sense. When one of the supports generates a
proper subgroup `H < G`, the BB construction effectively lives on
`F_2[H]`-cosets and the code factors as a direct product of smaller
BB codes (or trivially repeats them). Bravyi–Cross et al. (2308.07915)
implicitly require non-degenerate `A, B` for their constructions — the
gross polynomials each cover the full `Z_12 × Z_6`.

The classifier is intentionally cheap: BFS the subgroup closure under
the abelian group operation, stop on the first iteration that doesn't
add anything new, compare cardinality to `|G|`. For small BB groups
(`|G| ≤ 144` in the corpus), this is microseconds.

This module is read-only on its inputs — it does not modify polynomial
or group state.
"""

from __future__ import annotations

from .group import AbelianGroup
from .poly import Poly


def _subgroup_closure(
    generators: frozenset[tuple[int, ...]], G: AbelianGroup
) -> set[tuple[int, ...]]:
    """BFS the additive subgroup `⟨generators⟩ ≤ G`.

    The closure is the smallest subset of G containing 0 and closed
    under addition with each generator. (Since `G` is finite abelian
    and additively-written, taking inverses is automatic: every element
    has finite additive order.)

    Algorithm: start from `{0}`; iteratively add `h + g` for every
    already-known `h` and every generator `g`. Stop when no new
    element is added. Total iterations bounded by `|⟨generators⟩|`.
    """
    closure: set[tuple[int, ...]] = {tuple(0 for _ in G.orders)}
    if not generators:
        return closure
    frontier: set[tuple[int, ...]] = set(closure)
    while frontier:
        new_frontier: set[tuple[int, ...]] = set()
        for h in frontier:
            for g in generators:
                gh = G.add(h, g)
                if gh not in closure:
                    closure.add(gh)
                    new_frontier.add(gh)
        frontier = new_frontier
    return closure


def supp_generates_G(poly: Poly, G: AbelianGroup | None = None) -> bool:
    """Return True iff `⟨supp(poly)⟩ = G` (as additive subgroups of G).

    The closure is computed over the actual support set, regardless of
    F_2 coefficient values (which are all 1 by Poly's representation).

    Notes
    -----
    * The zero polynomial (empty support) generates only `{0}`; it
      returns `False` for any nontrivial `G`.
    * The constant polynomial `1` (support `{0}`) also generates only
      `{0}` — returns `False` for `|G| > 1`.
    * For Bravyi's gross polynomials `A = x³+y+y²`, `B = y³+x+x²` on
      `G = Z_12 × Z_6`, both generate the full G (return `True`).
    """
    if G is None:
        G = poly.group
    elif G != poly.group:
        raise ValueError("poly.group != G")
    closure = _subgroup_closure(poly.support, G)
    return len(closure) == G.cardinality


def support_subgroup_index(poly: Poly, G: AbelianGroup | None = None) -> int:
    """Return `[G : ⟨supp(poly)⟩]`.

    Equals 1 iff `poly` is non-degenerate (i.e. `supp_generates_G` is
    True). Useful as a diagnostic: large index means the polynomial
    lives in a small coset of G and the corresponding BB-block is a
    direct sum of smaller copies.

    Lagrange's theorem on abelian groups guarantees this index always
    divides `|G|`.
    """
    if G is None:
        G = poly.group
    elif G != poly.group:
        raise ValueError("poly.group != G")
    closure_size = len(_subgroup_closure(poly.support, G))
    if G.cardinality % closure_size != 0:
        raise RuntimeError(
            f"support_subgroup_index: closure size {closure_size} does not "
            f"divide |G| = {G.cardinality}; this violates Lagrange and "
            "indicates a bug in _subgroup_closure or AbelianGroup.add."
        )
    return G.cardinality // closure_size


def is_non_degenerate(
    A: Poly, B: Poly, G: AbelianGroup | None = None
) -> bool:
    """Return True iff **both** `A` and `B` have support generating G.

    This is the BB-code-level non-degeneracy condition: `BB(G, A, B)`
    is non-degenerate iff `⟨supp(A)⟩ = G = ⟨supp(B)⟩`.

    If either polynomial is degenerate (its support generates a proper
    subgroup), the BB construction either factors through a smaller
    group or produces an effectively-smaller code. The corpus has been
    observed to split roughly 50/50 between non-degenerate and
    degenerate codes (see notes/T3R2.2_nondegenerate_eval.md).
    """
    if G is None:
        G = A.group
    if A.group != B.group:
        raise ValueError("A and B must live in the same group algebra")
    return supp_generates_G(A, G) and supp_generates_G(B, G)
