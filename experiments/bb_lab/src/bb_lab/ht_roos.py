"""Hartmann–Tzeng (HT, 1972) and Roos (1983) lower bounds for the
minimum distance of cyclic and multivariate-cyclic codes, applied
to the polynomial check matrices of BB codes.

Mathematical setup
==================

For a polynomial `A ∈ F_2[G]` with `G = Z_{n_1} × ... × Z_{n_m}` a
finite abelian group, the **annihilator code** is

    ker(M_A) := { v ∈ F_2^|G| : A · v = 0 }  ⊆  F_2^|G|.

`ker(M_A)` is a (multivariate) cyclic code over `F_2[G]`. Its
**defining set** in the textbook sense is the set of characters of G
on which the *generator polynomial of the code* vanishes — which, for
the annihilator perspective, is exactly the set of characters where
A is *non-zero*:

    defining_set(ker M_A) := { k ∈ Ĝ ≅ G : Â(χ_k) ≠ 0 } = `nv(A)`.

We separately define:

    T_A := zeros of A = { k ∈ Ĝ ≅ G : Â(χ_k) = 0 }.

So **HT/Roos applied to `nv(A) = Ĝ \\ T_A` gives `d(ker M_A) ≥ ...`**
(a lower bound on `d_A^⊥ = min wt ker M_A`).

The classical Hartmann–Tzeng bound (1972) lower-bounds `d(C)` of any
cyclic code C from the structure of its defining set T:

  • **BCH bound**: if `T ⊇ {b + i·s : 0 ≤ i ≤ δ−2}` for some `b, s` with
    `gcd(s, n) = 1`, then `d(C) ≥ δ`.
  • **Hartmann–Tzeng (HT, 1972)**: if `T ⊇ {b + i·s_1 + j·s_2 : 0 ≤ i ≤
    δ−2, 0 ≤ j ≤ ν}` with `gcd(s_1, n) = 1`, `gcd(s_2, n) ≤ δ − 1`,
    then `d(C) ≥ δ + ν`.
  • **Roos (1983)**: further generalization allowing non-arithmetic
    sub-patterns; computationally heavier, often gives small additional
    improvement over HT.

The **multivariate** generalization (Saints–Heegard 1995, Sabin–Lomonaco
1992, Bernal–Bueno-Carreño–Simón 2016) extends the bound to the case
`G = Z_{n_1} × ... × Z_{n_m}`. There the HT pattern becomes an
**AP pattern in `Ĝ`** along two step vectors `s_1, s_2 ∈ G`. The
precise statement we use here is:

  • **Multivariate HT (Camion + Sabin–Lomonaco shape)**: if `T_A ⊇
    {b + i·s_1 + j·s_2 : 0 ≤ i ≤ δ−2, 0 ≤ j ≤ ν} ⊆ G`
    where: (i) `s_1` has order ≥ δ in G (so the i-direction generates
    a chain of length δ); (ii) `s_2` has order ≥ ν+1 in G; and (iii)
    `i·s_1 + j·s_2` are all distinct group elements for the indices
    in question, then `d(C_A) ≥ δ + ν`.

  This is the natural extension where `gcd(s, n) = 1` becomes "the
  subgroup generated by `s` is large enough" in the multivariate case.

  Note: the **Camion apparent distance** is a stricter notion: it is
  the maximum δ such that *every* cyclic shift of T_A (under the
  G-translation action on `Ĝ`) admits a multivariate-BCH chain.
  Apparent distance ≤ true `d_A^⊥`. Bernal et al. 2016 give a fast
  algorithm; here we implement a more direct brute-force "longest
  HT pattern" search, which is computationally feasible for
  `|G| ≤ 200`.

Why this matters for BB codes
=============================

Textbook CSS theory says (see HANDOFF §6h):

    d_X(BB(A, B))  ≤  min(d_A^⊥, d_B^⊥).

HT/Roos gives **lower bounds** on the per-block `d_A^⊥`, `d_B^⊥`:

    d_A^⊥  ≥  mv_ht(T_A),     d_B^⊥  ≥  mv_ht(T_B).

Composing the two **does not** give a positive bound on `d_X`. To
get one, we need an additional structural argument that the
*minimum-weight X-logical* is dominated by a single-block witness
in `ker(M_A^T) \\ rowspan(H_X-block-A)` (or symmetric for B). When
that holds, `d_X = min(d_A^⊥, d_B^⊥) ≥ min(mv_ht(T_A), mv_ht(T_B))`.

The structural condition `S(A, B)` is the object of T2R3.3:
`bb_ht_condition`. See `bb_ht_bound` for the headline candidate.

Frobenius orbits and the defining set
=====================================

Over `F_2`, the character `χ_k` lifts to `F̄_2` only when its order
in `Ĝ` is coprime to 2. For `|G|` odd, every character lifts and
the defining set `T_A` is partitioned by **Frobenius orbits**
(orbits of `k → 2k` on `Ĝ`). For `|G|` even, only characters whose
order is odd lift; the semisimple quotient `F_2[G_odd]` captures
the lifted part. We define `T_A` on the **G_odd projection**:

    T_A := { k_odd ∈ Ĝ_odd : Â(χ_{k_odd}) = 0 in F̄_2 }.

`T_A` is closed under the Frobenius orbit action, so it's a union
of Frobenius orbits. (This is the same set computed by
`algebraic_features.vanishing_orbits`.)

The multivariate HT pattern search then runs on `T_A ⊆ G_odd`.

Conventions
===========

  • `defining_set(poly, G)` returns `T_A` as a `frozenset[tuple[int, ...]]`
    of `G_odd` elements (so identical to the union of vanishing orbits).
  • `bch_bound(T, orders)`: classical BCH bound for univariate cyclic
    codes (single-axis case).
  • `ht_bound(T, n)`: classical HT bound for univariate cyclic codes.
  • `mv_ht_bound(T, orders)`: multivariate HT bound. For univariate
    `orders = (n,)` this matches `ht_bound`. For multivariate,
    searches over all candidate step vectors `(s_1, s_2)`.
  • `bb_ht_bound(A, B, G)`: composite candidate single-block-dominance
    lower bound on `d_X`. Returns `min(mv_ht(T_A), mv_ht(T_B))` when
    the structural condition holds; otherwise the trivial bound 1.
  • `bb_ht_condition(A, B, G)`: returns the structural condition
    verdict.

References
==========

- Hartmann & Tzeng 1972: "Generalizations of the BCH bound",
  Information and Control 20, 489–498.
- Roos 1983: "A new lower bound for the minimum distance of a cyclic
  code", IEEE Trans. Info. Theory 29, 330–332.
- Camion 1970: "Abelian codes", MRC Tech. Sum. Rep. 1059.
- Sabin & Lomonaco 1992: "Metacyclic Error-Correcting Codes", AAECC.
- Saints & Heegard 1995: "Algebraic-geometric codes and multidimensional
  cyclic codes: A unified theory and algorithms for decoding using
  Gröbner bases", IEEE TIT 41(6).
- Bernal, Bueno-Carreño & Simón 2016: "Apparent Distance and a Notion
  of BCH Multivariate Codes", IEEE TIT 62(2), 655–668.
"""

