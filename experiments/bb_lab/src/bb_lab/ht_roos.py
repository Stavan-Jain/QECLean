# ---------------------------------------------------------------------------
# Defining set of a polynomial in F_2[G]
# ---------------------------------------------------------------------------


def defining_set(
    poly: Poly,
    G: AbelianGroup | None = None,
) -> frozenset[tuple[int, ...]]:
    """Return the **zeros defining set** `T_A ⊆ G_odd` of the polynomial
    A in F_2[G], i.e. the set of characters `χ_k ∈ Ĝ_odd` on which A
    vanishes in F̄_2.

    Mathematical context
    --------------------

    For a polynomial `A ∈ F_2[G]` viewed as the generator of the principal
    ideal `C_A = ⟨A⟩`, the cyclic code `C_A` has `T_A` as its defining set
    (zeros of A). The **annihilator code** `ker(M_A) = {v : A·v = 0}` has
    the **complementary** defining set `Ĝ \\ T_A` (non-zeros of A).

    For HT/Roos applied to `d_A^⊥ = d(ker M_A)`, use
    `nonvanishing_set(poly, G)` (which returns `Ĝ \\ T_A`). For
    HT/Roos applied to `d(C_A) = d(⟨A⟩)`, use this `defining_set`.

    Notation caveats
    ----------------

    `T_A` is closed under the Frobenius action `k → 2k` on `G_odd`, so
    it is a union of Frobenius orbits. The vanishing condition is
    checked via `algebraic_features.jacobson_radical_depth` ≥ 1.

    For odd `|G|`, `T_A` is the standard cyclic-code defining set in
    `Ĝ`. For even `|G|`, only odd-order characters lift to F̄_2; the
    2-Sylow-character part is invisible at this level.

    Parameters
    ----------
    poly : Poly in F_2[G]
    G : AbelianGroup, optional. Defaults to poly.group.

    Returns
    -------
    frozenset[tuple[int, ...]]
        The set of G_odd elements where A vanishes.
    """
    if G is None:
        G = poly.group
    elif G != poly.group:
        raise ValueError("poly.group != G")
    orbits = g_odd_frobenius_orbits(G)
    T: set[tuple[int, ...]] = set()
    for orbit in orbits:
        mu = jacobson_radical_depth(poly, orbit, G)
        if mu >= 1:
            T.update(orbit)
    return frozenset(T)


def nonvanishing_set(
    poly: Poly,
    G: AbelianGroup | None = None,
) -> frozenset[tuple[int, ...]]:
    """Return the **non-zeros set** `Ĝ_odd \\ T_A ⊆ G_odd` of A in F_2[G].

    This is the defining set of the **annihilator code** `ker(M_A) =
    {v : A·v = 0}`. Applying `mv_ht_bound` to this set gives a lower
    bound on `d_A^⊥ = d(ker M_A) = min_wt_ker_A`.

    Concretely: a character χ ∈ Ĝ_odd is in `nonvanishing_set(A)` iff
    `Â(χ) ≠ 0` in F̄_2. The annihilator `ker M_A` consists of vectors
    v whose Fourier transform vanishes on `nonvanishing_set(A)` (it
    can be free elsewhere).

    Caveat
    ------

    For odd `|G|`, `nonvanishing_set` is the exact "defining set of
    the annihilator". For even `|G|`, this is the defining set of the
    **semisimple-quotient annihilator**, and the bound `d_A^⊥ ≥
    mv_ht(nonvanishing_set)` may be loose by the radical contribution.
    """
    if G is None:
        G = poly.group
    elif G != poly.group:
        raise ValueError("poly.group != G")
    n_odds = _g_odd_orders(G)
    all_g_odd = frozenset(_enumerate_group(n_odds))
    return frozenset(all_g_odd - defining_set(poly, G))


# ---------------------------------------------------------------------------
# Univariate Hartmann–Tzeng bound
# ---------------------------------------------------------------------------


def bch_bound(T: set[int] | frozenset[int], n: int) -> int:
    """Classical BCH bound on the minimum distance of a univariate
    cyclic code of length `n` with defining set `T ⊆ Z_n`.

    The classical Hartmann–Tzeng formulation of BCH (also called the
    "generalized BCH" or "ν=0 HT" bound): if `T ⊇ {b + i·s : 0 ≤ i ≤
    δ−2}` for some `b, s ∈ Z_n` with `gcd(s, n) = 1`, then `d ≥ δ`.

    Returns the largest such δ over all (b, s).

    Special cases:
      • `s = 1`: classical "consecutive" BCH (longest run of integers).
      • `T = ∅`: returns 1 (trivial bound).
      • `T = Z_n`: returns `n+1` (sentinel for trivial/zero code).

    Parameters
    ----------
    T : set or frozenset of int
        The defining set as a subset of Z_n. (Mod-n arithmetic applied.)
    n : int
        Length of the cyclic code.

    Returns
    -------
    int
        The BCH bound. Always ≥ 1.

    Examples
    --------
    >>> bch_bound({1, 2, 3, 4}, 15)  # 4 consecutive ⟹ d ≥ 5
    5
    >>> bch_bound({1, 2, 4, 8}, 15)  # Frobenius orbit; step-3 chain {1,4} ⟹ δ=3
    3
    """
    if not T:
        return 1
    T_mod = frozenset(k % n for k in T)
    if len(T_mod) == n:
        return n + 1
    best = 1
    for s in range(1, n):
        if gcd(s, n) != 1:
            continue
        for b in T_mod:
            chain_len = 0
            cur = b
            visited: set[int] = set()
            while cur in T_mod and cur not in visited:
                visited.add(cur)
                cur = (cur + s) % n
                chain_len += 1
            # chain_len elements ⟹ δ − 1 = chain_len ⟹ δ = chain_len + 1.
            cand = chain_len + 1
            if cand > best:
                best = cand
    return best


def ht_bound(T: set[int] | frozenset[int], n: int) -> int:
    """Classical Hartmann–Tzeng bound on the minimum distance of a
    univariate cyclic code of length `n` with defining set `T ⊆ Z_n`.

    HT 1972: if `T ⊇ {b + i·s_1 + j·s_2 : 0 ≤ i ≤ δ−2, 0 ≤ j ≤ ν}` for
    some `b, s_1, s_2 ∈ Z_n` with `gcd(s_1, n) = 1` and `gcd(s_2, n) ≤
    δ − 1`, then `d ≥ δ + ν`.

    For `s_2 = s_1` (or `ν = 0`) the bound reduces to BCH along step
    `s_1` (which by gcd condition gives the same chain length as the
    consecutive case `s_1 = 1`).

    We search over all (s_1, s_2) and (b) and return the maximum
    `δ + ν` found.

    Parameters
    ----------
    T : set or frozenset of int
        The defining set as a subset of Z_n.
    n : int
        Length of the cyclic code.

    Returns
    -------
    int
        The HT bound. Always ≥ `bch_bound(T, n)` since BCH = HT at ν = 0.

    Notes
    -----
    Brute-force complexity is `O(n^3 · δ_max · ν_max)`, fine for
    `n ≤ 200`. For larger n use the smarter Bernal–Bueno-Carreño
    algorithm (not implemented here).
    """
    if not T:
        return 1
    T_mod = frozenset(k % n for k in T)
    if len(T_mod) == n:
        return n + 1

    best = bch_bound(T_mod, n)  # at least the BCH bound

    # Search over candidate step vectors (s_1, s_2) and offsets b.
    for s_1 in range(1, n):
        if gcd(s_1, n) != 1:
            continue
        for b in T_mod:
            # Walk i = 0, 1, ... while (b + i*s_1) mod n is in T.
            chain_len = 0
            cur = b
            visited: set[int] = set()
            while cur in T_mod and cur not in visited:
                visited.add(cur)
                cur = (cur + s_1) % n
                chain_len += 1
            # chain_len = δ − 1 (number of distinct chain points), so δ = chain_len + 1.
            ht_delta = chain_len + 1
            if ht_delta < 2:
                continue
            # BCH gives δ on this chain.
            if ht_delta > best:
                best = ht_delta
            if ht_delta == 2:
                # δ - 1 = 1; no room to extend with ν.
                continue
            # ν candidates: HT's gcd(s_2, n) ≤ δ − 1.
            for s_2 in range(1, n):
                if s_2 == s_1:
                    continue
                if gcd(s_2, n) > ht_delta - 1:
                    continue
                # Walk j-axis: largest ν such that for all 0 ≤ i ≤ δ−2 (= chain_len-1),
                # all 0 ≤ j ≤ ν, the point b + i*s_1 + j*s_2 ∈ T.
                # By distinctness HT-pattern points must be unique mod n;
                # we enforce both T-membership and uniqueness across (i, j).
                grid: set[int] = set()
                # j = 0 row: b + i*s_1 for i = 0..chain_len-1.
                cur_i = b
                for _ in range(chain_len):
                    grid.add(cur_i)
                    cur_i = (cur_i + s_1) % n
                nu = 0
                for j in range(1, n):
                    row_ok = True
                    row_pts: list[int] = []
                    base = (b + j * s_2) % n
                    cur_i = base
                    for _ in range(chain_len):
                        if cur_i not in T_mod or cur_i in grid:
                            row_ok = False
                            break
                        row_pts.append(cur_i)
                        cur_i = (cur_i + s_1) % n
                    if not row_ok:
                        break
                    grid.update(row_pts)
                    nu = j
                cand = ht_delta + nu
                if cand > best:
                    best = cand
    return best


# ---------------------------------------------------------------------------
# Multivariate HT bound on G = Z_{n_1} × Z_{n_2} × ...
# ---------------------------------------------------------------------------


def _gv_add(g: tuple[int, ...], h: tuple[int, ...], orders: tuple[int, ...]) -> tuple[int, ...]:
    """Group addition on Z_{n_1} × ..., per-axis mod."""
    return tuple((gi + hi) % n for gi, hi, n in zip(g, h, orders))


def _gv_scalar(c: int, g: tuple[int, ...], orders: tuple[int, ...]) -> tuple[int, ...]:
    """Scalar multiplication c·g in the additive abelian group."""
    return tuple((c * gi) % n for gi, n in zip(g, orders))


def _gv_order(g: tuple[int, ...], orders: tuple[int, ...]) -> int:
    """Order of element g in the abelian group (smallest positive m with
    m·g = 0). For g = 0 returns 1 by convention."""
    if all(gi == 0 for gi in g):
        return 1
    # Order is lcm over axes of (n_axis / gcd(g_axis, n_axis)).
    from math import lcm
    o = 1
    for gi, n in zip(g, orders):
        if gi == 0:
            o_axis = 1
        else:
            d_axis = gcd(gi, n)
            o_axis = n // d_axis
        o = lcm(o, o_axis)
    return o


def mv_bch_bound(
    T: set[tuple[int, ...]] | frozenset[tuple[int, ...]],
    orders: tuple[int, ...],
) -> int:
    """Multivariate BCH bound for an F_2 cyclic code over G = Z_{n_1} ×
    ... × Z_{n_m}: maximum δ such that T contains an arithmetic
    progression of length δ−1 along a step `s_1 ∈ G` **whose additive
    order equals |G|** (i.e., `s_1` generates the full group).

    The "step generates full group" condition is the multivariate
    analog of the univariate `gcd(s_1, n) = 1`. For G cyclic of order n,
    such s_1 are exactly the elements coprime to n. For G = Z_a × Z_b,
    s_1 generates G iff `gcd(a, b) = 1` AND s_1's image in each axis
    is coprime to the axis order — equivalent to the LCM of axis-orders
    being |G|, which requires coprimality of all axis orders.

    For G non-cyclic (e.g. Z_3 × Z_6 where gcd(3, 6) = 3 > 1), NO
    element generates the full G, so this bound only fires when T
    contains an AP along a step that fully lies in T — which is
    very restrictive. In particular, the function returns 1 if no
    full-G-generating element exists.

    For univariate `orders = (n,)`, matches `bch_bound`.

    Parameters
    ----------
    T : set of tuples. Defining set ⊆ G.
    orders : tuple[int, ...]. Cyclic factor orders.

    Returns
    -------
    int
        The multivariate BCH bound. ≥ 1 always.

    Notes
    -----
    This corrects an earlier overly-permissive implementation that
    accepted step vectors of any order ≥ 2. The "full group generator"
    requirement matches Saints-Heegard 1995 / Sabin-Lomonaco 1992 for
    the multivariate-cyclic-code case.
    """
    if not T:
        return 1
    T_set = frozenset(T)
    n_G = 1
    for n in orders:
        n_G *= n
    if len(T_set) == n_G:
        return n_G + 1

    best = 1
    for s_1 in _enumerate_group(orders):
        if all(si == 0 for si in s_1):
            continue
        ord_s1 = _gv_order(s_1, orders)
        if ord_s1 != n_G:
            # s_1 must generate the full G for HT/BCH to apply.
            continue
        for b in T_set:
            chain_len = 0
            cur = b
            visited: set[tuple[int, ...]] = set()
            while cur in T_set and cur not in visited:
                visited.add(cur)
                cur = _gv_add(cur, s_1, orders)
                chain_len += 1
                if chain_len > ord_s1:
                    break
            cand = chain_len + 1
            if cand > best:
                best = cand
    if best == 1 and T_set:
        # Even with a singleton in T, the "nonempty defining set" gives d ≥ 2
        # IF some full-G generator exists. Otherwise return 1.
        for s in _enumerate_group(orders):
            if _gv_order(s, orders) == n_G:
                return 2
    return best


def _enumerate_group(orders: tuple[int, ...]):
    """Yield every element of Z_{n_1} × ... × Z_{n_m} (lex order)."""
    if not orders:
        yield ()
        return

    def rec(axis: int, prefix: list[int]):
        if axis == len(orders):
            yield tuple(prefix)
            return
        for i in range(orders[axis]):
            prefix.append(i)
            yield from rec(axis + 1, prefix)
            prefix.pop()

    yield from rec(0, [])


def mv_ht_bound(
    T: set[tuple[int, ...]] | frozenset[tuple[int, ...]],
    orders: tuple[int, ...],
) -> int:
    """Multivariate Hartmann–Tzeng bound for an F_2 cyclic code over
    G = Z_{n_1} × ... × Z_{n_m}.

    Cf. Saints–Heegard 1995 / Sabin–Lomonaco 1992 for the multivariate
    generalization. The bound: if `T ⊇ {b + i·s_1 + j·s_2 : 0 ≤ i ≤
    δ−2, 0 ≤ j ≤ ν}` where
      (a) s_1 has additive order = |G| (s_1 generates the full G),
      (b) s_2 has additive order > ν, and
      (c) the points b + i·s_1 + j·s_2 are distinct in G,
    then the minimum distance of the cyclic code `C(T)` (= the F_2
    cyclic code with defining set T) satisfies `d ≥ δ + ν`.

    **Restriction**: this bound requires G to admit a full-G-generating
    element, i.e., G is cyclic OR the cyclic factor orders are pairwise
    coprime. For G = Z_a × Z_b with gcd(a, b) > 1, no element generates
    G; the bound then reduces to "T nonempty ⟹ d ≥ 2" (because at most
    the trivial single-point AP applies).

    Brute-force search over (s_1, s_2, b). Complexity `O(|G|^4)`,
    safe up to `|G| ≤ 200`.

    For univariate `orders = (n,)` this matches `ht_bound`.

    Parameters
    ----------
    T : set of tuples
        Defining set as subset of G.
    orders : tuple[int, ...]
        Cyclic group orders.

    Returns
    -------
    int
        The multivariate HT bound. ≥ `mv_bch_bound(T, orders)`.

    Notes
    -----
    Earlier permissive versions of this function (accepting any
    step-vector of order ≥ 2) produced FALSE LOWER BOUNDS: e.g. for
    T = {(0, b) : b ∈ Z_5} in Z_3 × Z_5, they returned 6, but the
    actual code's min distance is 2 (because the code includes
    vectors with two cells in the same y-fiber). The "step generates
    full group" condition fixes this.
    """
    if not T:
        return 1
    T_set = frozenset(T)
    n_G = 1
    for n in orders:
        n_G *= n
    if len(T_set) == n_G:
        return n_G + 1

    best = mv_bch_bound(T_set, orders)

    for s_1 in _enumerate_group(orders):
        if all(si == 0 for si in s_1):
            continue
        ord_s1 = _gv_order(s_1, orders)
        if ord_s1 != n_G:
            continue  # s_1 must generate full G

        for b in T_set:
            chain_pts: list[tuple[int, ...]] = []
            cur = b
            seen: set[tuple[int, ...]] = set()
            while cur in T_set and cur not in seen:
                chain_pts.append(cur)
                seen.add(cur)
                cur = _gv_add(cur, s_1, orders)
                if len(chain_pts) > ord_s1:
                    break
            chain_len = len(chain_pts)
            ht_delta = chain_len + 1
            if ht_delta < 2:
                continue
            if ht_delta > best:
                best = ht_delta
            if ht_delta < 3:
                continue

            for s_2 in _enumerate_group(orders):
                if all(si == 0 for si in s_2):
                    continue
                if s_2 == s_1:
                    continue
                ord_s2 = _gv_order(s_2, orders)
                # For HT, classical condition is gcd(s_2, n) ≤ δ - 1 in
                # the univariate case. The multivariate analog (Saints-
                # Heegard) requires s_2's order > ν. We enforce this
                # implicitly via the j-loop bound below.

                nu = 0
                grid_points: set[tuple[int, ...]] = set(chain_pts)
                for j in range(1, ord_s2):
                    new_row_ok = True
                    new_row_pts: list[tuple[int, ...]] = []
                    for i in range(chain_len):
                        offset = _gv_scalar(j, s_2, orders)
                        pt = _gv_add(chain_pts[i], offset, orders)
                        if pt not in T_set:
                            new_row_ok = False
                            break
                        if pt in grid_points:
                            new_row_ok = False
                            break
                        new_row_pts.append(pt)
                    if not new_row_ok:
                        break
                    grid_points.update(new_row_pts)
                    nu = j
                cand = ht_delta + nu
                if cand > best:
                    best = cand
    return best


