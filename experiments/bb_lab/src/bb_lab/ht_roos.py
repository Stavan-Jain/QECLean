def bb_ht_per_block_bound(A: Poly, B: Poly, G: AbelianGroup | None = None) -> tuple[int, int]:
    """Return `(mv_ht(nonvanishing(A)), mv_ht(nonvanishing(B)))`, the
    HT lower bounds on the per-block classical dual distances
    `d_A^⊥, d_B^⊥`.

    Note (crucial!): the HT bound on `d_A^⊥ = d(ker M_A)` uses the
    **non-zeros defining set** `Ĝ_odd \\ T_A`, NOT `T_A` itself.
    See `nonvanishing_set` for the reason.

    **Caveat — semisimple case only**: The HT bound `d_A^⊥ ≥
    mv_ht(nonvanishing(A))` is a valid lower bound only when `|G|` is
    odd (i.e. `F_2[G]` is semisimple). For even `|G|`, the Jacobson
    radical contributes extra low-weight kernel elements that HT
    cannot see; the bound is then a lower bound on `d_A^⊥_odd` (the
    distance in the semisimple quotient) only.

    The `bb_ht_condition` gates the even-|G| case out as
    `G_not_semisimple`.
    """
    if G is None:
        G = A.group
    if A.group != B.group:
        raise ValueError("A and B must live in the same group algebra")
    n_odds = _g_odd_orders(G)
    nv_A = nonvanishing_set(A, G)
    nv_B = nonvanishing_set(B, G)
    return mv_ht_bound(nv_A, n_odds), mv_ht_bound(nv_B, n_odds)


def _is_semisimple(G: AbelianGroup) -> bool:
    """Return True iff F_2[G] is semisimple, i.e. `gcd(|G|, 2) = 1`."""
    return G.cardinality % 2 == 1


def bb_ht_condition(
    A: Poly,
    B: Poly,
    G: AbelianGroup | None = None,
    *,
    d_exact: int | None = None,
    max_kernel_dim: int = 22,
) -> tuple[bool, str]:
    """Check the structural single-block-dominance condition `S(A, B)`
    under which the chain

        d_X  =  min(d_A^⊥, d_B^⊥)  ≥  min(mv_ht(T_A), mv_ht(T_B))

    becomes a valid lower bound on `d_X`.

    Definition
    ----------

    `S(A, B)` holds iff the **textbook CSS upper bound is tight**:

        d_X(BB(A, B))  =  min(d_A^⊥, d_B^⊥).

    Equivalently: every minimum-weight nontrivial X-logical is a
    single-block witness `(f, 0)` with `f ∈ ker(M_B^T) \\ rowspan(H_X-A-block)`
    (or symmetric for B-block). There is no mixed-block witness of
    lower weight.

    Computational notes
    -------------------

    This condition is **descriptive, not predictive**: checking it
    requires knowing `d_X` (which is an exact SAT computation). For
    the Tier 2 mill, we use:

      • If `d_exact` is provided (from corpus): condition := `d_exact ==
        min(d_A^⊥, d_B^⊥)`. Otherwise return `(False, "no_d_exact")`.

      • Without `d_exact`: a heuristic — `S(A, B)` plausible iff
        `joint_kernel_min_weight(A, B) > min(d_A^⊥, d_B^⊥)` (the joint
        single-block witness is heavier than per-block, suggesting
        no mixed-block beats it). This is a **necessary** condition
        but not sufficient — it doesn't rule out small mixed-block
        witnesses we'd want to dominate. Treat its `True` verdict as
        "plausible, needs SAT verification".

    Both verdicts are returned along with a diagnostic string.

    Parameters
    ----------
    A, B : Poly in F_2[G].
    G : AbelianGroup, optional. Defaults to A.group.
    d_exact : int, optional. If provided, used as ground truth.
    max_kernel_dim : int. Skip if d_A^⊥ requires too-big enumeration.

    Returns
    -------
    (bool, str)
        First component: True if condition holds (or plausible).
        Second: brief diagnostic.
    """
    if G is None:
        G = A.group
    if A.group != B.group:
        raise ValueError("A and B must live in the same group algebra")

    # Restriction: HT is a valid lower bound on per-block d_A^⊥ only when
    # F_2[G] is semisimple (|G| odd). For even |G|, the radical of F_2[G]
    # contributes additional small-weight kernel elements that HT cannot see.
    if not _is_semisimple(G):
        return False, "G_not_semisimple"

    M_A = circulant(A)
    M_B = circulant(B)
    # Bail if per-block kernel is too big to enumerate.
    try:
        d_A_perp = min_weight_in_kernel(M_A)
        d_B_perp = min_weight_in_kernel(M_B)
    except ValueError:
        return False, "block_kernel_too_big"
    min_block = min(d_A_perp, d_B_perp)
    if min_block <= 1:
        return False, "trivial_min_wt"

    if d_exact is not None:
        # Authoritative test.
        if d_exact == min_block:
            return True, "ok_exact"
        elif d_exact > min_block:
            # d_X > min_block is impossible (textbook upper bound);
            # would indicate bug.
            return False, "d_exact_exceeds_block_kernel_BUG"
        else:
            return False, f"d_exact={d_exact}_lt_min_block={min_block}"

    # Heuristic: necessary condition is that joint single-block witness
    # is no lighter than per-block. This doesn't rule out mixed-block
    # witnesses but at least eliminates the obvious "both blocks
    # annihilate by same v" failure case.
    import numpy as np

    combined = np.vstack([M_A, M_B]).astype(np.uint8)
    try:
        j_min = min_weight_in_kernel(combined)
    except ValueError:
        return False, "joint_kernel_too_big"
    if j_min < min_block:
        return False, "joint_kernel_lighter"
    # Heuristic positive: condition plausible.
    return True, "heuristic_plausible"


def bb_ht_bound(
    A: Poly,
    B: Poly,
    G: AbelianGroup | None = None,
    *,
    d_exact: int | None = None,
) -> int:
    """Composite candidate lower bound on `d_X(BB(A, B))`:

        d_X  ≥  min(mv_ht(T_A), mv_ht(T_B))  when `S(A, B)` holds,
        d_X  ≥  1                             otherwise.

    Per the prompt: this is a "constrained" bound that only fires
    when the textbook CSS upper bound `d_X ≤ min(d_A^⊥, d_B^⊥)` is
    tight. The HT bound provides a lower-bound *replacement* for
    the per-block kernel weight in that scenario.

    Parameters
    ----------
    A, B : Poly in F_2[G].
    G : AbelianGroup, optional. Defaults to A.group.
    d_exact : int, optional. Authoritative d_X for the condition check.

    Returns
    -------
    int
        The candidate lower bound. ≥ 1 always.

    Notes
    -----
    Tier-2 candidate. See `notes/T2R3.4_eval.md` for empirical
    behavior across the corpus.
    """
    if G is None:
        G = A.group
    if A.group != B.group:
        raise ValueError("A and B must live in the same group algebra")
    ok, _ = bb_ht_condition(A, B, G, d_exact=d_exact)
    if not ok:
        return 1
    ht_a, ht_b = bb_ht_per_block_bound(A, B, G)
    return min(ht_a, ht_b)
