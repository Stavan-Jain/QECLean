"""Emit MImAssembly.lean: the y-orbit dispatch (floor_kcombo, 64 cases) + mimBound_holds.
Each nonzero combo maps to (y-orbit rep i, shift k) with kcombo(combo)=translate(0,k)(kcombo rep_i)
(verified). fin_cases order is c0 MSB .. c5 LSB. Usage: PYTHONPATH=src python scripts/gen_assembly.py
"""
from __future__ import annotations
import numpy as np
from bb_lab.group import ZmZn
from bb_lab.poly import Poly
from bb_lab.checks import bb_check_matrices
from bb_lab.linalg import nullspace_f2

Gb = ZmZn(6, 6)
Ab = Poly.from_string("x^3 + y + y^2", Gb); Bb = Poly.from_string("y^3 + x + x^2", Gb)
d2b = (bb_check_matrices(Ab, Bb).H_Z & 1).T
ker2 = nullspace_f2(d2b)
def span(rows):
    out = []
    for m in range(1 << rows.shape[0]):
        v = np.zeros(rows.shape[1], np.uint8)
        for i in range(rows.shape[0]):
            if (m >> i) & 1: v ^= rows[i]
        out.append(v)
    return out
Z2all = [z for z in span(ker2) if z.any()]
FREE = [(4, 4), (4, 5), (5, 2), (5, 3), (5, 4), (5, 5)]
def combo(z): return tuple(int(z[Gb.index(c)]) for c in FREE)
def trY(z, k):
    o = np.zeros_like(z)
    for g in Gb: o[Gb.index(g)] = z[Gb.index((g[0], (g[1]+k) % 6))]
    return o
c2z = {combo(z): z for z in Z2all}
seen = set(); reps = []; disp = {}
for z in Z2all:
    c = combo(z)
    if c in seen: continue
    ri = len(reps); reps.append(c)
    for k in range(6):
        ck = combo(trY(z, k))
        if ck not in seen:
            seen.add(ck); disp[ck] = (ri, k)

def bits_of(n): return tuple((n >> (5 - j)) & 1 for j in range(6))  # c0 MSB .. c5 LSB

bullets = []
for n in range(64):
    c = bits_of(n)
    bs = " ".join(str(b) for b in c)
    if all(b == 0 for b in c):
        bullets.append("  · rw [kcombo_zero, seamC_zero, zero_add] at hb; exact absurd ⟨f, rfl⟩ hb")
    else:
        ri, k = disp[c]
        rb = " ".join(str(b) for b in reps[ri])
        bullets.append(
            f"  · rw [show kcombo {bs} =\n"
            f"        translate ((0, {k}) : BaseGroup) (kcombo {rb}) from by native_decide]\n"
            f"    exact floor_shiftYk_combo {rb} ({k} : ZMod 6) Y{ri}.floor f")

imports = "\n".join(
    f"import QEC.Stabilizer.Codes.BivariateBicycle.MImFloorY{i}" for i in range(13))
