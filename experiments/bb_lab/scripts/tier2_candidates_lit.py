def main() -> None:
    ap = argparse.ArgumentParser(
        description=(
            "Tier-2 literature-derived candidate distance bounds. "
            "Read-only against the corpus."
        ),
    )
    ap.add_argument(
        "--db", type=Path, default=None,
        help="Path to a bb_instances.duckdb (default: lab's data/bb_instances.duckdb)",
    )
    ap.add_argument(
        "--groups", nargs="*", default=None,
        help="Restrict to these group_struct labels.",
    )
    ap.add_argument(
        "--limit-rows", type=int, default=None,
        help=(
            "If set, randomly subsample at most this many rows before "
            "evaluating. Useful for fast smoke runs. Default: all."
        ),
    )
    args = ap.parse_args()

    corpus = Corpus(db_path=args.db) if args.db else Corpus()
    summary = corpus.summary()
    print(f"corpus: {summary}")

    if args.limit_rows is not None:
        # Honor by clamping the corpus via Corpus.limited; not really
        # random, just truncation, but enough for smoke tests.
        corpus = corpus.limited(int(args.limit_rows))

    reports = [
        evaluate_candidate(corpus, c, args.groups) for c in LIT_CANDIDATES
    ]
    print(render_report(reports))


if __name__ == "__main__":
    main()
