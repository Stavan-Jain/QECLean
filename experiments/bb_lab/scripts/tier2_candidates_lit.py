CSS_dual_textbook = Candidate(
    name="CSS_dual_textbook_re_cited",
    formula="d ≤ min(d_A^⊥, d_B^⊥) = min(min_wt_ker_A, min_wt_ker_B)",
    citation=(
        "Calderbank-Shor 1996 / Steane 1996 CSS distance theory: for "
        "any CSS code with H_X = [A|B], H_Z = [B^T|A^T], any nonzero "
        "f ∈ ker(A^T) gives the X-error (f, 0) satisfying H_Z (f,0)^T = 0, "
        "so |f| upper-bounds d_X. Symmetric for B. Explicitly stated "
        "in Lin-Pryadko 2023 arXiv:2306.16400 §III.C around Eq. (31), "
        "and re-derived in Kovalev-Pryadko 2013 §III. Same bound as "
        "`CSS_classical_dual` in tier2_explore.py; this entry exists "
        "to expose the textbook citation chain for the literature "
        "survey audit. DO NOT re-claim as novel."
    ),
    requires=("min_wt_ker_A", "min_wt_ker_B"),
    bound_fn=lambda r: min(int(r["min_wt_ker_A"]), int(r["min_wt_ker_B"])),
)


# Wang-Pryadko 2022 Statement 2 — cyclic-cover UPPER bound (GB-only).
#
#   d ≤ d_g,   d_g = distance of cyclic code with poly (x^ℓ - 1) / gcd(A, B, x^ℓ - 1)
#
# Specific to 1D-cyclic (GB) codes. For BB we'd need a multivariate
# gcd in F_2[Z_ℓ × Z_m], which is not yet a corpus feature. We DEFINE
