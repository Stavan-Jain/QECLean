"""Tier-2 literature-derived distance bound candidates.

Companion to `tier2_explore.py`. This file collects additional
`Candidate` records derived from the literature survey in
`experiments/bb_lab/notes/T2.3_literature_survey.md`. **Every
candidate here is established prior art** — none are claimed as
novel by this work. The citation field gives the originating
theorem/statement and section reference.

Why a separate file? Sibling Tier-2 tasks may edit
`tier2_explore.py`; keeping the literature-survey candidates here
avoids merge conflicts. To use them, import `LIT_CANDIDATES` and
pass to `evaluate_candidate` / `render_report` from
`tier2_explore.py`, or run this module directly via

    uv run python scripts/tier2_candidates_lit.py

Each candidate matches the `Candidate` dataclass in
`tier2_explore.py`. Some candidates require auxiliary features
(`c = |G_a ∩ G_b|`, `|G_a|`, `|G_b|`) that are NOT in the corpus
schema today; for those we compute the features on-the-fly from
`A_poly`, `B_poly`, `ell`, `m` columns and use a lambda that does
the lift. This is read-only and does not modify the corpus.

All literature bounds collected here are UPPER bounds on `d` unless
explicitly labeled "lower" (the TZ lower bound is one such case).
For lower bounds, the convention used by `evaluate_candidate` is
that `bound_fn` returns a value where the bound is `d_exact ≥ b`
— i.e., violations occur when `b > d_exact`. We document
direction per candidate.

References (see notes/T2.3_literature_survey.md for full bibliography):

* LP23 = Lin-Pryadko 2023, arXiv:2306.16400 ("Quantum two-block group algebra codes")
* KP13 = Kovalev-Pryadko 2013, arXiv:1212.6703 ("Quantum hyperbicycle LDPC codes")
* Bravyi24 = Bravyi-Cross-Gambetta-Maslov-Rall-Yoder 2024, arXiv:2308.07915
* PK21 = Panteleev-Kalachev 2021, arXiv:2012.04068
* WP22 = Wang-Pryadko 2022, arXiv:2203.17216 ("Distance bounds for GB codes")
* RDV25 = Raveendran-Declercq-Vasic 2025, arXiv:2503.07567
* CSS96 = Calderbank-Shor 1996 / Steane 1996 (CSS textbook bounds)
"""

