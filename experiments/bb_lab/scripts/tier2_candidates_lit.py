# Trivially derivable from k=2·dim(ker A ∩ ker B) (Bravyi24 Lemma 1):
#
#   d ≤ dim(ker A ∩ ker B) = k / 2
#
# This is a structural consequence of CSS theory (any subset of qubits
# of size > k/2 trivializes one logical), but it's listed here because
# Bravyi24 Lemma 1 is the explicit citation. For BB codes specifically.
#
# Actually, more carefully: dim(ker A ∩ ker B) is the codespace
# dimension on either block — and any linear combo of those kernel
# vectors gives a low-weight logical IF that combo is not in
# rowspan(H_X). The naive `d ≤ dim_ker_AB` is too aggressive; the
# correct CSS bound is `d ≤ minwt(ker A ∩ ker B \ rowspan(H_X))` which
# would require a search. We DON'T include the unsafe naive form;
# instead we note the relationship as an annotated remark in the file
# docstring. (Skipped here.)


