def _support_subgroup_order(supp: frozenset[tuple[int, ...]], G: ZmZn) -> int:
    """Order of the subgroup of G generated by the support `supp`.

    Brute-force BFS closure. For our corpus group orders (|G| <= 72
    at the moment), this is trivially fast — closure over a 72-element
    group takes microseconds. For larger lifts, we'd cache by
    `(supp, ell, m)`.
    """
    orders = G.orders
    identity = tuple(0 for _ in orders)
    elements = {identity}
    frontier = list(supp)
    while frontier:
        nxt: list[tuple[int, ...]] = []
        for g in list(elements):
            for h in supp:
                gh = tuple((gi + hi) % oi for gi, hi, oi in zip(g, h, orders))
                gmh = tuple((gi - hi) % oi for gi, hi, oi in zip(g, h, orders))
                if gh not in elements:
                    elements.add(gh)
                    nxt.append(gh)
                if gmh not in elements:
                    elements.add(gmh)
                    nxt.append(gmh)
        frontier = nxt
    return len(elements)


def _intersection_subgroup_order(
    suppA: frozenset[tuple[int, ...]],
    suppB: frozenset[tuple[int, ...]],
    G: ZmZn,
) -> int:
    """Order of `G_a ∩ G_b` where G_a = ⟨supp(A)⟩, G_b = ⟨supp(B)⟩."""
    orders = G.orders
    identity = tuple(0 for _ in orders)
    def closure(supp: frozenset[tuple[int, ...]]) -> set[tuple[int, ...]]:
        out = {identity}
        frontier = list(supp)
        while frontier:
            nxt: list[tuple[int, ...]] = []
            for g in list(out):
                for h in supp:
                    gh = tuple((gi + hi) % oi for gi, hi, oi in zip(g, h, orders))
                    gmh = tuple((gi - hi) % oi for gi, hi, oi in zip(g, h, orders))
                    if gh not in out:
                        out.add(gh)
                        nxt.append(gh)
                    if gmh not in out:
                        out.add(gmh)
                        nxt.append(gmh)
            frontier = nxt
        return out
    Ga = closure(suppA)
    Gb = closure(suppB)
    return len(Ga & Gb)


def _supp_orders_from_row(r: dict) -> tuple[int, int, int]:
    """For a corpus row, return (|G_a|, |G_b|, |G_a ∩ G_b|).

    Parses `A_poly`, `B_poly` strings to recover supports, then runs
    BFS closure over `ZmZn(ell, m)`. Pure read-only.
    """
    G = ZmZn(int(r["ell"]), int(r["m"]))
    A = Poly.from_string(r["A_poly"], G)
    B = Poly.from_string(r["B_poly"], G)
    Ga_ord = _support_subgroup_order(A.support, G)
    Gb_ord = _support_subgroup_order(B.support, G)
    c = _intersection_subgroup_order(A.support, B.support, G)
    return Ga_ord, Gb_ord, c


