# LP23 Statement 12 / KP13 Theorem 5 — Tillich-Zemor LOWER bound.
#
#   d ≥ ⌈min(d_A^⊥, d_B^⊥) / c⌉,   c = |G_a ∩ G_b|.
#
# Direction: LOWER bound on d. We model it as an "upper bound" in the
# evaluate_candidate sense by INVERTING: violations occur if the
# computed value EXCEEDS d_exact. The looseness `bound - d` for a
# lower bound is `≤ 0` when the bound holds, with `0` = tight.
# To fit the existing evaluate_candidate harness (which expects upper
# bounds), we expose this as `cand_TZ_lower` with the bound function
# returning `⌈min(d_A^⊥, d_B^⊥)/c⌉`. The harness will report
# "looseness" = bound − d_exact, so for a lower bound a NEGATIVE
# value means the bound holds; a positive value indicates a
# **violation**, which would be a bug or counterexample.
#
# Practically: every row with `bound_TZ_lower <= d_exact` SUPPORTS the
# bound. We add a note in the citation field to keep this clear.
TZ_lower_bound = Candidate(
    name="TZ_lower_LP23_Stmt12",
    formula="d ≥ ⌈min(d_A^⊥, d_B^⊥) / c⌉,  c = |G_a ∩ G_b|  (LOWER bound)",
    citation=(
        "Lin-Pryadko 2023 arXiv:2306.16400 Statement 12 (§IV.F), "
        "and equivalently Kovalev-Pryadko 2013 arXiv:1212.6703 Theorem 5 "
        "(Eq. 32). Tight when c = 1 (LP23 §IV.F explicit). NOTE: this "
        "is a LOWER bound — the bound_fn returns ⌈d_0/c⌉ which should "
        "be ≤ d_exact. In the harness, looseness = (bound − d_exact); "
        "values ≤ 0 confirm the bound, > 0 would indicate a violation."
    ),
    requires=("min_wt_ker_A", "min_wt_ker_B", "A_poly", "B_poly", "ell", "m"),
    bound_fn=lambda r: max(
        1,
        math.ceil(
            min(int(r["min_wt_ker_A"]), int(r["min_wt_ker_B"]))
            / max(_supp_orders_from_row(r)[2], 1)
        ),
    ),
)


# LP23 §III.C post-Stmt-10 — support-group UPPER ceiling.
#
#   d ≤ min(|G_a|, |G_b|).
#
# Computable from corpus: parses A_poly, B_poly and computes the
# subgroup-closure orders. Direction: UPPER bound on d.
support_group_ceiling = Candidate(
    name="support_group_ceiling_LP23",
    formula="d ≤ min(|G_a|, |G_b|)  (UPPER bound; LP23 §III.C)",
    citation=(
        "Lin-Pryadko 2023 arXiv:2306.16400 §III.C (just after Stmt 10): "
        "'d ≤ min(|G_a|, |G_b|)' under conditions of Stmt 8/10 (abelian "
        "2BGA, which always holds for BB codes). G_a, G_b are the "
        "support subgroups generated by the supports of A, B in G."
    ),
    requires=("A_poly", "B_poly", "ell", "m"),
    bound_fn=lambda r: min(*_supp_orders_from_row(r)[:2]),
)


# LP23 §IV.F post-Stmt-13 — Tillich-Zemor index UPPER ceiling.
#
#   d_0 ≤ c · min(ℓ_a, ℓ_b),  ℓ_a = [G_a : N], ℓ_b = [G_b : N].
#
# This caps the *TZ lower bound* d_0, NOT d itself directly.
# But: d ≥ d_0 (the lower bound) AND d_0 ≤ c · min(ℓ_a, ℓ_b)
# together don't directly upper-bound d. However, LP23 derives in the
# same paragraph that the corresponding UPPER bound on d itself is:
#   d_Z ≤ c · min(ℓ_a, ℓ_b)
# via the two-sided coset maximal-ideal construction (Stmt 13 with
# specific ideal choice). We use it as an upper bound here.
#
# Computable: c = |N|, ℓ_a = |G_a|/c, ℓ_b = |G_b|/c.
TZ_index_ceiling = Candidate(
    name="TZ_index_ceiling_LP23",
    formula="d ≤ c · min(ℓ_a, ℓ_b),  c = |N|, ℓ_a = [G_a:N], ℓ_b = [G_b:N]",
    citation=(
        "Lin-Pryadko 2023 arXiv:2306.16400 §IV.F (post Stmt 13): "
        "'d_0 ≤ min(ℓ_a, ℓ_b)' and the matching upper-bound chain "
        "'d_Z ≤ c min(ℓ_a, ℓ_b)' obtained from Stmt 13 + Stmt 4 "
        "block-diagonal argument. For the gross polynomial pair this "
        "evaluates to d ≤ 8 · 3 = 24 (vs. d_exact = 12), so the "
        "bound IS loose on gross but does hold."
    ),
    requires=("A_poly", "B_poly", "ell", "m"),
    bound_fn=lambda r: (
        lambda gA, gB, c: c * min(gA // max(c, 1), gB // max(c, 1))
    )(*_supp_orders_from_row(r)),
)


# Wang-Pryadko 2022 Statement 13 / Otjens 2025 Theorem 2.18 —
# asymptotic D-locality UPPER bound.
#
#   d ≤ n^(1 - 1/(w - 1)),   w = stabilizer row weight = A_weight + B_weight.
#
# This is asymptotic: the literature does NOT pin down the
# multiplicative constants. As a "weak-asymptotic candidate" we use the
# naked formula and flag that violations are expected on small n
# where the asymptotic doesn't apply yet.
#
# Direction: UPPER bound (asymptotic).
WP_locality_asymptotic = Candidate(
    name="WP_locality_asymptotic_O_n_pow",
    formula=(
        "d ≤ ⌊n^(1 − 1/(w−1))⌋,  w = A_weight + B_weight  "
        "(ASYMPTOTIC; constants unspecified)"
    ),
    citation=(
        "Wang-Pryadko 2022 arXiv:2203.17216 Statement 13; same shape "
        "as Otjens 2025 arXiv:2502.17052 Theorem 2.18 (d ≤ 2√γ_D (√D + 4) "
        "n^(1 − 1/D), D = Δ − 2). Both are big-O asymptotic; the naked "
        "n^(1 − 1/(w−1)) is the dominant term. EXPECT violations on "
        "small n where multiplicative constants are not yet absorbed; "
        "this candidate is included as a baseline for upper-bound "
        "asymptotic looseness comparison, NOT as a finite-N sharp bound."
    ),
    requires=("n", "A_weight", "B_weight"),
    bound_fn=lambda r: (
        # Guard w−1 ≥ 1
        int(math.floor(
            int(r["n"]) ** (1 - 1 / max(int(r["A_weight"]) + int(r["B_weight"]) - 1, 2))
        ))
    ),
)


# CSS textbook (Calderbank-Shor / Steane) — re-encoded explicitly so
# the candidate file is self-contained for the literature survey.
# This duplicates `CSS_classical_dual` in tier2_explore.py but with a
