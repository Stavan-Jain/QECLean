"""A12 Phase C: which weight-3 BB cover pairs violate (R)?

By Theorem A12 (note §3), for a free Z2 BB cover with deck s = x^(L/2)
    (R) fails  <=>  k(cover) > k(base)  <=>  1 + x^(L/2) not in (A,B),
so one F2 rank test per pair decides (R).

IMPORTANT FRAMING: the quantification is over *cover* pairs — weight-3
(A, B) on Z_L x Z_m with L even.  (Enumerating base pairs and lifting
canonically misses covers whose exponents exceed the base order: the
cover polynomial, not the base pair, determines (R).)

Sweeps (both with A, B normalized to contain the monomial 1; unordered
pairs — every invariant here is symmetric under unit scaling and A<->B):

1. GENERAL: all weight-3 cover pairs on a grid of cover groups.
2. STRICT IBM SHAPE: A = x^a + y^b + y^c, B = y^d + x^e + x^f with all
   exponents >= 1 (b < c, e < f) on the cover.

Failure classification:
  - dead-block: some odd character kills every deck-sector component of
    both A and B (the construction of note §2).  Detected by testing
    whether the ideal generated by all P-sector components in F2[G_odd]
    is proper (P = 2-part of the cover group).
  - subtle: failure without a dead block (possible only when the 2-part
    exceeds Z2; the (delta, eps*delta)-type blocks of note §4).
  - axis flags: 'decoupled' / 'single-var' / 'one-sided' as before.

Run: python3 scripts/a12_weight3_class_sweep.py   (from experiments/bb_lab)
"""

import itertools
import math
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from a12_deck_r_probe import BBGroup, rref_basis, in_span  # noqa: E402


def membership(L, m, A, B):
    """Is 1 + x^(L/2) in the ideal (A, B) of F2[Z_L x Z_m]?"""
    G = BBGroup(L, m)
    MA = G.poly_matrix(A)
    MB = G.poly_matrix(B)
    target = (1 << G.idx(0, 0)) ^ (1 << G.idx(L // 2, 0))
    return in_span(target, rref_basis(MA + MB))


def odd_part(n):
    while n % 2 == 0:
        n //= 2
    return n


def has_dead_block(L, m, A, B):
    """Does some odd character kill all 2-part-sector components of A, B?

    Decompose each monomial x^i y^j as (2-part coords) x (odd coords);
    collect, per 2-part cell, the odd-part polynomial of each of A, B;
    a dead block exists iff the ideal generated by ALL these components
    in F2[G_odd] is proper (rank < |G_odd|)."""
    lo, mo = odd_part(L), odd_part(m)
    l2, m2 = L // lo, m // mo
    Godd = BBGroup(lo, mo)
    # CRT: i mod L  <->  (i mod l2, i mod lo); component key = 2-part cell
    comps = {}
    for poly in (A, B):
        for idx, (i, j) in enumerate(poly):
            key = (id(poly), i % l2, j % m2)
            comps.setdefault(key, []).append((i % lo, j % mo))
    cols = []
    for mono_list in comps.values():
        base_cols = Godd.poly_matrix(mono_list)
        cols.extend(base_cols)
    return len(rref_basis(cols)) < lo * mo


def axis_class(A, B):
    def axis_locked(poly):
        if len({a for (a, _) in poly}) == 1:
            return "y"
        if len({b for (_, b) in poly}) == 1:
            return "x"
        return None

    la, lb = axis_locked(A), axis_locked(B)
    if la and lb:
        return "decoupled" if la != lb else "single-var"
    if la or lb:
        return "one-sided"
    return "mixing"


def record_failure(stats, samples, L, m, A, B):
    dead = has_dead_block(L, m, A, B)
    cls = axis_class(A, B)
    key = ("dead" if dead else "SUBTLE", cls)
    stats[key] = stats.get(key, 0) + 1
    if not dead and len(samples) < 10:
        samples.append((L, m, A, B))


def sweep_general():
    print("== GENERAL weight-3 cover-pair sweep (deck = x^(L/2)) ==")
    grid = [(L, m) for L in (2, 4, 6) for m in (2, 3, 4, 5, 6)]
    grid += [(8, 2), (8, 3), (8, 4), (8, 5), (10, 3), (12, 3)]
    stats, samples = {}, []
    total = fails = 0
    for L, m in grid:
        monos = [(i, j) for i in range(L) for j in range(m) if (i, j) != (0, 0)]
        polys = [[(0, 0), p, q]
                 for p, q in itertools.combinations(monos, 2)]
        n_fail = n_pairs = 0
        for A, B in itertools.combinations_with_replacement(polys, 2):
            n_pairs += 1
            if not membership(L, m, A, B):
                n_fail += 1
                record_failure(stats, samples, L, m, A, B)
        total += n_pairs
        fails += n_fail
        print(f"  Z{L}xZ{m}: {n_pairs:>8d} pairs, {n_fail:>6d} failures",
              flush=True)
    print(f"GENERAL total: {total} pairs, {fails} failures")
    print(f"  by (mechanism, axis-class): {stats}")
    if samples:
        print("  sample SUBTLE failures (L, m, A, B):")
        for row in samples:
            print(f"    {row}")
    return stats, samples


def sweep_ibm():
    print("\n== STRICT IBM-shape cover-pair sweep "
          "(A = x^a+y^b+y^c, B = y^d+x^e+x^f, exponents >= 1) ==")
    grid = [(L, m) for L in (2, 4, 6, 8) for m in (2, 3, 4, 5, 6, 7)]
    grid += [(10, 7), (12, 7), (14, 7)]
    stats, samples = {}, []
    total = fails = 0
    for L, m in grid:
        As = [[(a, 0), (0, b), (0, c)]
              for a in range(1, L)
              for b, c in itertools.combinations(range(1, m), 2)]
        Bs = [[(0, d), (e, 0), (f, 0)]
              for d in range(1, m)
              for e, f in itertools.combinations(range(1, L), 2)]
        n_fail = n_pairs = 0
        for A, B in itertools.product(As, Bs):
            n_pairs += 1
            if not membership(L, m, A, B):
                n_fail += 1
                record_failure(stats, samples, L, m, A, B)
        total += n_pairs
        fails += n_fail
        if n_fail:
            print(f"  Z{L}xZ{m}: {n_pairs:>7d} pairs, {n_fail:>5d} failures",
                  flush=True)
    print(f"IBM total: {total} pairs, {fails} failures")
    print(f"  by (mechanism, axis-class): {stats}")
    if samples:
        print("  sample SUBTLE IBM failures (L, m, A, B):")
        for row in samples:
            print(f"    {row}")
    return stats, samples


if __name__ == "__main__":
    g_stats, g_samples = sweep_general()
    i_stats, i_samples = sweep_ibm()
    print("\nSUMMARY")
    print("  general:", g_stats)
    print("  IBM:    ", i_stats)
